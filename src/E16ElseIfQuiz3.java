public class E16ElseIfQuiz3 {
    public static void main(String[] args) {
        int num=3;

        if(num>0) {
            System.out.println("Number is positive");
        }
        else if(num<0) {
            System.out.println("Number is negative");
        }
        else {
            System.out.println("Zero");
        }
    }
}
